export interface IndustryInfo {
  /** 真实行业因子 = 行业平均工资 / 城市全行业平均 */
  factor: number;
  /** 该城市该行业的年平均工资（元），0 表示无数据 */
  avgSalary: number;
}

/** 获取城市某行业的真实因子和平均工资（直接用官方行业名） */
export function getIndustryInfo(city: string, industry: string): IndustryInfo {
  const cityData = CITY_INDUSTRY_DATA[city];

  if (!cityData) {
    return { factor: 1.0, avgSalary: 0 };
  }

  const industrySalary = cityData[industry];
  if (!industrySalary) {
    return { factor: 1.0, avgSalary: 0 };
  }

  const salaries = Object.values(cityData);
  const cityAvg = salaries.reduce((a, b) => a + b, 0) / salaries.length;

  return {
    factor: industrySalary / cityAvg,
    avgSalary: industrySalary,
  };
}

/** 获取支持行业薪资数据的城市列表 */
export function getIndustrySalaryCities(): string[] {
  return Object.keys(CITY_INDUSTRY_DATA);
}
